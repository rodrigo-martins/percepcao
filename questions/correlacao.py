from pathlib import Path
from typing import Optional
import numpy as np
import pandas as pd

try:
    import matplotlib.pyplot as plt
    import seaborn as sns
except Exception:
    plt = None
    sns = None


def criar_heatmap_correlacao(corr_matrix: pd.DataFrame, out_path: Optional[Path] = None, 
                               cmap: str = "RdBu_r", figsize: tuple = (14, 12),
                               annot_fontsize: int = 10, title_fontsize: int = 14) -> Optional[Path]:
    """
        Cria heatmap de correlação policórica com rótulos Q1, Q2, ..., sem barra de escala.
        Método: Bayes modal estimation (Choi, Kim, Chen, & Dannels, 2011).
         
        Args:
            corr_matrix: DataFrame com matriz de correlação policórica (27x27)
            out_path: caminho para salvar a imagem (default: output/correlacao_heatmap.png)
            cmap: colormap (default: RdBu_r — azul negativo, vermelho positivo)
            figsize: tamanho da figura (w, h)
            annot_fontsize: tamanho da fonte das anotações (default: 10)
            title_fontsize: tamanho da fonte do título (default: 14)
    
    Returns:
        Path do arquivo salvo ou None se falhar
    """
    if plt is None or sns is None:
        print("matplotlib ou seaborn não disponíveis")
        return None
    
    out_path = Path(out_path) if out_path is not None else Path("output/correlacao_heatmap.png")
    out_path.parent.mkdir(parents=True, exist_ok=True)
    
    # renomear índices/colunas para Q1, Q2, ...
    n = len(corr_matrix)
    new_labels = [f"Q{i+1}" for i in range(n)]
    corr_display = corr_matrix.copy()
    corr_display.index = new_labels
    corr_display.columns = new_labels
    
    fig, ax = plt.subplots(figsize=figsize)
    
    # criar heatmap com anotações, sem cbar
    sns.heatmap(corr_display, annot=True, fmt=".3f", cmap=cmap, center=0.0,
                vmin=-1, vmax=1, ax=ax, cbar=False,
                annot_kws={"fontsize": annot_fontsize},
                linewidths=0.5, linecolor="white")
    
    # ax.set_title("Matriz de Correlação Policórica\n(Bayes Modal Estimation; Choi, Kim, Chen, & Dannels, 2011)", 
    #              fontsize=title_fontsize, fontweight="bold", pad=20)
    # ax.set_xlabel("Questões", fontsize=14, fontweight="bold")
    # ax.set_ylabel("Questões", fontsize=14, fontweight="bold")
    
    # ajustar rótulos de eixo
    ax.set_xticklabels(ax.get_xticklabels(), rotation=0, fontsize=12)
    ax.set_yticklabels(ax.get_yticklabels(), rotation=0, fontsize=12)
    
    plt.tight_layout()
    fig.savefig(out_path, dpi=300, bbox_inches="tight")
    plt.close(fig)
    
    print(f"Heatmap salvo em: {out_path}")
    return out_path


